class Solution:

    def cal_coin(self,i,k,j):
        return self.nums[i]*self.nums[k]*self.nums[j]

    def dfs(self,i,j):
        if i>j:return 0
        if self.dp[i][j]:return self.dp[i][j]
        for k in range(i,j+1): ## lastboon
            self.dp[i][j] = max(self.dp[i][j], self.dfs(i,k-1) + self.dfs(k+1,j) + self.cal_coin(i-1,k,j+1))
        return self.dp[i][j]

    def maxCoins(self, nums: List[int]) -> int:
        self.nums = [1] + nums + [1]
        n = len(nums) + 2
        self.dp = [[0]*n for _ in range(n)]
        self.dfs(1,n-2)
        return self.dp[1][n-2]

